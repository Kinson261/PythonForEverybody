"""
Exercise 5

 Write a program which prompts the user for a Celsius temperature, convert
 the temperature to Fahrenheit, and print out the converted temperature.
"""

print('\n\n________________________________________')
print('Exercise 5')

celsius = float(input('Enter temperature in celsius: \t'))
fahrenheit = (celsius * 1.80) + 32

print('Temperature in fahrenheit: \t', fahrenheit)

input()
